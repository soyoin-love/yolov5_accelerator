        .mcif_req_vld               (f_mcif_req_vld),
        .mcif_req_pd                (f_mcif_req_pd),
        .mcif_rx_vld                (f_mcif_rx_vld),
        .mcif_rx_dat                (f_mcif_rx_dat),
        .mcif_rx_rdy                (f_mcif_rx_rdy),
        .wr_en                      (cbuf_wr_en),
        .wr_row                     (cbuf_wr_row),
        .wr_col_blk                 (cbuf_wr_col_blk),
        .wr_ch_grp                  (cbuf_wr_ch_grp),
        .wr_bank_en                 (cbuf_wr_bank_en),
        .wr_bank_dat                (cbuf_wr_bank_dat),
        .wr_row_done                (cbuf_wr_row_done)
    );

    b_cdma_top #(
        .BANK_NUM           (BANK_NUM),
        .AXI_DATA_WIDTH     (AXI_DATA_WIDTH),
        .MAX_BURST_LEN      (MAX_BURST_LEN),
        .LOG2_MAX_BURST_LEN (LOG2_MAX_BURST_LEN),
        .BBUF_ADDR_W        (BBUF_ADDR_W)
