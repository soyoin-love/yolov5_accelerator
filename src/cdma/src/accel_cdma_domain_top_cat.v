        .wbuf_wr_offset       (wbuf_wr_offset),
        .wbuf_wr_dat          (wbuf_wr_dat),
        .wbuf_wr_region_done  (wbuf_wr_region_done)
    );

    cdma_top_cat #(
        .ROW_BLOCKS(ROW_BLOCKS),
        .BANK_NUM  (BANK_NUM),
        .TK_IN     (TK_IN),
        .N         (N)
    ) u_f_cdma (
