    w_cdma_top #(
        .BANK_NUM(BANK_NUM),
        .REGION_W(REGION_W),
        .AXI_DATA_WIDTH(AXI_DATA_WIDTH),
        .MAX_BURST_LEN(MAX_BURST_LEN),
        .LOG2_MAX_BURST_LEN(LOG2_MAX_BURST_LEN)
    ) u_w_cdma (
        .clk                  (clk),
        .rst_n                (rst_n),
        .start                (w_start),
        .done                 (w_done),
