        .done            (b_done),
        .cfg_b_base_addr (cfg_b_base_addr),
        .cfg_out_channels(cfg_b_out_channels),
        .rd_req_vld      (b_rd_req_vld),
        .rd_req_rdy      (b_rd_req_rdy),
        .rd_req_pd       (b_rd_req_pd),
        .rd_resp_vld     (b_rd_resp_vld),
        .rd_resp_rdy     (b_rd_resp_rdy),
        .rd_resp_pd      (b_rd_resp_pd),
        .bbuf_wr_en      (bbuf_wr_en),
        .bbuf_wr_addr    (bbuf_wr_addr),
        .bbuf_wr_dat     (bbuf_wr_dat)
